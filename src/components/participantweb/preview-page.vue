export default {
  name: "preview-page",
  props: {
    studyData: Object
  },
  data() {
    return {
      globalElements: {
        id: "",
        studyId: "",
        status: "",
        siteTitle: "",
        primaryColor: "",
        secondaryColor: "",
        image_file: "test.png",
        privacyPolicy: "",
        licenseAgreement: "",
        facilityName: "",
        contactName: "",
        contactEmail: "",
        contactPhoneNumber: "",
        contactFaxNumber: "",
        sponsorLogo: ""
      }
    };
  },
  computed: {
    ...mapGetters(["token", "user"]),
    userAccess() {
      return new AclRule(this.user.userRole).generate();
    },
    userToken() {
      return this.token;
    },
    studyId() {
      return this.$route.params.studyId;
    },
    getStatuses() {
      return participantStatuses;
    },
    getStatusesText() {
      return participantStatusesText;
    },
    canPiAccess() {
      this.$acl.change(`${PI}`);
      return this.$acl.check("userAccess");
    },
    canSubPIAccess() {
      this.$acl.change(`${SUB_PI}`);
      return this.$acl.check("userAccess");
    },
    canStudyCoordinatorAccess() {
      this.$acl.change(`${STUDY_COORDINATOR}`);
      return this.$acl.check("userAccess");
    },
    canQualityAccess() {
      this.$acl.change(`${QUALITY}`);
      return this.$acl.check("userAccess");
    },
    canHomeHealthAccess() {
      this.$acl.change(`${HOME_HEALTH}`);
      return this.$acl.check("userAccess");
    },
    canDMAccess() {
      this.$acl.change(`${DM}`);
      return this.$acl.check("userAccess");
    },
    canCRAccess() {
      this.$acl.change(`${CRA}`);
      return this.$acl.check("userAccess");
    }
  },
  watch: {
    studyData(newData, oldData) {
      if (newData.countryList) {
        this.studyData.countryList = sortBy(newData.countryList, "sortOrder");
      }
      if (newData !== oldData) {
        this.getStudyDetails();
      }
    }
  },
  created() {
    axios({
      url: `http://localhost:3000/api/v1/studymanagement/studies/${this.$route.params.studyId}/web/globalelements`,
      method: "GET"
    })
      .then(response => {
        if (response) {
          console.log("get respose", response);

          (this.globalElements.siteTitle = response.data.data.siteTitle),
            (this.globalElements.image_file = response.data.data.studyLogo),
            (this.globalElements.primaryColor =
              response.data.data.primaryColor),
            (this.globalElements.secondaryColor =
              response.data.data.secondaryColor),
            (this.globalElements.privacyPolicy =
              response.data.data.privacyPolicy);
          // this.globalElements.licenseAgreement = response.data.data.licenseAgreement,
          (this.globalElements.contactName = response.data.data.contactName),
            (this.globalElements.contactPhoneNumber =
              response.data.data.contactPhoneNumber),
            (this.globalElements.contactFaxNumber =
              response.data.data.contactFaxNumber),
            (this.globalElements.facilityName =
              response.data.data.facilityName),
            (this.globalElements.contactEmail =
              response.data.data.contactEmail);
        }
      })
      .catch(err => {
        throw err;
      });
  }
};
